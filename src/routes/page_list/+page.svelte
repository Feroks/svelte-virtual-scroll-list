<script>
    import {VirtualScroll} from "$lib"
    import {createSequenceGenerator, randomInteger} from "../mock"
    import TestItem from "../TestItem.svelte"

    const getItemId = createSequenceGenerator()

    let items = $state([])
    addItems(true, 1000)

    let list = $state()

    function addItems(top = true, count = 10) {
        let new_items = []
        for (let i = 0; i < count; i++)
            new_items.push({uniqueKey: getItemId(), height: randomInteger(20, 60)})
        if (top)
            items = [...new_items, ...items]
        else
            items = [...items, ...new_items]
    }
</script>
<div class="overflow-buttons">
    <button onclick={() => list.scrollToOffset(0)}>To top</button>
    <button onclick={list.scrollToBottom}>To bottom</button>
</div>
<div class="vs">
    <VirtualScroll
            bind:this={list}
            data={items}
            key="uniqueKey"
            pageMode={true}
    >
        {#snippet header()}
            <div >
                This is a header
            </div>
        {/snippet}
        {#snippet children({ data })}
            <TestItem {...data}/>
        {/snippet}
        {#snippet footer()}
            <div >
                This is a footer
            </div>
        {/snippet}
    </VirtualScroll>
</div>

<style>
    .overflow-buttons {
        z-index: 2;
        top: 10px;
        position: sticky;
    }
</style>
